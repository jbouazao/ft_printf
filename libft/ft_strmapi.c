#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	char			*begin;
	unsigned int	i;

	i = 0;
	if (!s)
		return (NULL);
	str = (char *)malloc(ft_strlen(s) + 1);
	if (!str)
		return (NULL);
	begin = str;
	while (*s)
	{
		*str = (*f)(i, *s);
		str++;
		s++;
		i++;
	}
	*str = '\0';
	return (begin);
}
