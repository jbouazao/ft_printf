#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	char	temp;

	if (s1 && s2)
		while (*s1 || *s2)
		{
			temp = *s1 - *s2;
			if (temp > 0 || temp < 0)
				return (0);
			s1++;
			s2++;
		}
	return (1);
}
